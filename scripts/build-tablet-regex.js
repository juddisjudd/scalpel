#!/usr/bin/env node
/**
 * Build src/shared/data/regex/vendor/tablets/Tablet.Gen.ts from poe2.re's
 * Generated.Tablet.min.json (veiset/poe2.re). Mirrors the waystone data pipeline.
 * Used with attribution. Re-run when upstream tablet data changes:
 *   node scripts/build-tablet-regex.js
 */
const https = require('node:https')
const fs = require('node:fs')
const path = require('node:path')

const URL = 'https://raw.githubusercontent.com/veiset/poe2.re/main/public/generated/Generated.Tablet.min.json'
const OUT = path.join(__dirname, '..', 'src', 'shared', 'data', 'regex', 'vendor', 'tablets', 'Tablet.Gen.ts')

function fetch(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, { headers: { 'User-Agent': 'Scalpel' } }, (res) => {
        if (res.statusCode === 301 || res.statusCode === 302) return fetch(res.headers.location).then(resolve, reject)
        if (res.statusCode !== 200) {
          res.resume()
          return reject(new Error(`HTTP ${res.statusCode} for ${url}`))
        }
        let data = ''
        res.on('data', (c) => {
          data += c
        })
        res.on('end', () => resolve(data))
      })
      .on('error', reject)
  })
}

/** Verbatim port of poe2.re's parseAffixToken (src/lib/parseAffixToken.ts). */
function parseAffixToken(token) {
  const ranges = []
  const values = []
  const lines = token.rawText.split('\n')
  const processed = lines.map((line) => {
    let out = line
    out = out.replace(/\(([+-]?\d+)-([+-]?\d+)\)/g, (_m, a, b) => {
      ranges.push([Number(a), Number(b)])
      return '##'
    })
    out = out.replace(/(?<![A-Za-z0-9])\+?(\d+)(?![A-Za-z0-9])/g, (_m, n) => {
      values.push(Number(n))
      return '#'
    })
    out = out.replace(/\[([^\]]+)\]/g, '$1')
    return out
  })
  return {
    id: token.id,
    name: processed.join('|'),
    regex: token.regex,
    values,
    ranges,
    prefix: !!token.options.prefix,
    tags: token.options.tags ?? [],
  }
}

async function main() {
  const json = JSON.parse(await fetch(URL))
  const mods = json.tokens.map(parseAffixToken).sort((a, b) => a.name.localeCompare(b.name))
  const header = `// Data sourced from poe2.re (https://github.com/veiset/poe2.re)
// Used with permission. All credit to veiset and contributors.
// Generated by scripts/build-tablet-regex.js -- do not edit by hand.

export interface TabletRegex {
  id: number
  name: string
  regex: string
  values: number[]
  ranges: number[][]
  prefix: boolean
  tags: string[]
}

export const tabletRegex: TabletRegex[] = `
  fs.mkdirSync(path.dirname(OUT), { recursive: true })
  fs.writeFileSync(OUT, header + JSON.stringify(mods, null, 2) + '\n')
  console.log(`wrote ${OUT} (${mods.length} tablet mods)`)
}

main().catch((err) => {
  console.error('[build-tablet-regex] failed:', err.message)
  process.exit(1)
})
